#gaurdrail for prompt detection:-
def detect_prompt_injection(text):

    suspicious_phrases = [
        "ignore previous instructions",
        "you are now",
        "act as",
        "apologize to me",
        "change your role"
    ]
    text = text.lower()
    for phrase in suspicious_phrases:
        if phrase in text:
            return True
    return False

#rag context:-
