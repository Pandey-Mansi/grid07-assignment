from typing import TypedDict
from langgraph.graph import StateGraph

#query search tool:-
def mock_search(query):
    q = query.lower()
    if "ai" in q:
        return "New AI model may automate junior developer tasks and replace them."
    elif "crypto" in q:
        return "Bitcoin reaches new hieght after ETF approvals."
    elif "market" in q:
        return "Markets swing as interest rates rises."
    elif "privacy" in q:
        return "Rapidly it's becoming concerning about social media data privacy."
    else:
        return "Technology discussions are trending and that's the main part in today's world."

#graph botstates:-
class BotState(TypedDict):
    bot_id: str
    persona: str
    topic: str
    search_result: str
    post_content: dict

#decide amongs topic:-
def decide_topic(state):
    persona = state["persona"].lower()
    if "finance" in persona or "roi" in persona:
        topic = "market trends"
    elif "privacy" in persona or "society" in persona:
        topic = "data privacy"
    else:
        topic = "latest AI developments"
    print("Chosen Topic:", topic)
    return {
        "topic": topic
    }

#web searching:-
def run_search(state):
    result = mock_search(
        state["topic"]
    )
    print("Search Context:", result)
    return {
        "search_result": result
    }

#draft post for selected topic:-
def generate_post(state):
    persona = state["persona"].lower()
    topic = state["topic"]
    context = state["search_result"]
    if "finance" in persona:
        post = (
            f"{context}. Markets reward those who act early and smartly."
            f"Everything is about ROI."
        )
    elif "privacy" in persona:
        post = (
            f"{context}. Big tech keeps proving regulation matters and evolving over time."
        )
    else:
        post = (
            f"{context}. Innovation moves faster than people realize."
        )
    post = post[:280]


    output_json = {
        "bot_id": state["bot_id"],
        "topic": topic,
        "post_content": post
    }
    return {
        "post_content": output_json
    }

#langraph main part:-
graph = StateGraph(BotState)
graph.add_node(
    "decide",
    decide_topic
)

graph.add_node(
    "search",
    run_search
)

graph.add_node(
    "draft",
    generate_post
)

graph.set_entry_point(
    "decide"
)

graph.add_edge(
    "decide",
    "search"
)

graph.add_edge(
    "search",
    "draft"
)

graph.set_finish_point(
    "draft"
)
app = graph.compile()

