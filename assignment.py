# Phase 1: Vector-Based Persona Matching (The Router):-
import numpy as np
import faiss
from sentence_transformers import SentenceTransformer

# bot personas:-
bot_personas = {
    "bot_A": "I believe AI and crypto will solve all human problems. I am highly optimistic about technology, Elon Musk, and space exploration. I dismissregulatory concerns.",
    "bot_B": "I believe late-stage capitalism and tech monopolies are destroying society. I am highly critical of AI, social media, and billionaires. I value privacy and nature.",
    "bot_C": "I strictly care about markets, interest rates, trading algorithms, and making money. I speak in finance jargon and view everything through the lens of ROI."
}

# loading embedding model:-
model = SentenceTransformer("all-MiniLM-L6-v2")

# vectorization of personas:-
persona_texts = list(bot_personas.values())
persona_ids = list(bot_personas.keys())
persona_embeddings = model.encode(persona_texts)

# indexing(FAISS):-
dim = persona_embeddings.shape[1]
index = faiss.IndexFlatIP(dim)

# normalization:-
faiss.normalize_L2(persona_embeddings)
index.add(persona_embeddings)


# routing:-
def route_post_to_bots(text, threshold=0.1):
    post_vec = model.encode([text])
    faiss.normalize_L2(post_vec)

    # differentiating top matches:-
    scores, indices = index.search(post_vec, k=3)

    print("\nDebug Info:-")
    print("Scores:", scores)
    print("Indices:", indices)
    selected = []

    for score, idx in zip(scores[0], indices[0]):
        if score >= threshold:
            bot_name = persona_ids[idx]
            selected.append((bot_name, float(score)))
    return selected


